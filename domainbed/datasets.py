class PSST(MultipleDomainDataset):
    INPUT_SHAPE = (10,50)
    ENVIRONMENTS = ['0','1','ood']

    def convert_examples_to_features(
            self,
            examples: List[dict],
            vocab: torchtext.vocab.Vectors,
            device: 'cuda',
            embedding_func=None
    ):
        """Convert examples to torch.Tensors of (text, offsets, labels)."""
        text, labels = [], []
        
        colors = []
        for example in examples:
            # input
            words = example['text'].split()
            #print(words)
            word_ids = [vocab.stoi[word] for word in words if word in vocab.stoi]
            if len(word_ids) < 1:
                continue
            elif len(word_ids) > 10:
                word_ids = word_ids[len(word_ids)-10:len(word_ids)]
            elif len(word_ids) < 10:
                word_ids2 = []
                for i in range(10-len(word_ids)):
                    word_ids2.append(0)
                word_ids2.extend(word_ids)
                word_ids = word_ids2
            text.append(word_ids)
            # label
            labels.append(int(example['label']))
            if words[-1] =='.':
                colors.append(1)
            else:
                colors.append(0)

        text = torch.tensor(text).to(device)
        labels = torch.tensor(labels).to(device)
        
        colors = torch.tensor(colors).to(device)
        hidden = embedding_func(text)
        return {'features':hidden, 'labels':labels, 'colors':colors}

    def __init__(self, root, test_env, hparams):
        super().__init__()
        self.input_shape = self.INPUT_SHAPE
        self.num_classes = 2
        self.datasets = []
        device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
        data_dir = os.path.join(root, "PSST")
        train_examples = get_train_examples(data_dir)
        test_examples = get_test_examples(data_dir)
        vocab = torchtext.vocab.GloVe(name="6B", dim=50)
        embeddings = vocab.vectors
        embedding_func = torch.nn.Embedding.from_pretrained(embeddings, freeze=True, padding_idx=0).to("cuda")
        train_envs = {env_name: self.convert_examples_to_features(examples, vocab, device, embedding_func)
                  for env_name, examples in train_examples.items()}
        test_envs = {env_name: self.convert_examples_to_features(examples, vocab, device, embedding_func)
                 for env_name, examples in test_examples.items()}
        env0_data = torch.cat([train_envs['train0']['features'],test_envs['test0']['features']])
        env0_label = torch.cat([train_envs['train0']['labels'],test_envs['test0']['labels']])
        env1_data = torch.cat([train_envs['train1']['features'],test_envs['test1']['features']])
        env1_label = torch.cat([train_envs['train1']['labels'],test_envs['test1']['labels']])
        envood_data = test_envs['test_ood']['features']
        envood_label = test_envs['test_ood']['labels']
        # print(env0_data.shape)
        self.datasets.append(TensorDataset(env0_data,env0_label))
        self.datasets.append(TensorDataset(env1_data,env1_label))
        self.datasets.append(TensorDataset(envood_data,envood_label))


class MultipleEnvironmentMNIST(MultipleDomainDataset):
    def __init__(self, root, environments, dataset_transform, input_shape,
                 num_classes):
        super().__init__()
        if root is None:
            raise ValueError('Data directory not specified!')

        original_dataset_tr = MNIST(root, train=True, download=True)
        original_dataset_te = MNIST(root, train=False, download=True)

        original_images = torch.cat((original_dataset_tr.data,
                                     original_dataset_te.data))

        original_labels = torch.cat((original_dataset_tr.targets,
                                     original_dataset_te.targets))

        shuffle = torch.randperm(len(original_images))

        original_images = original_images[shuffle]
        original_labels = original_labels[shuffle]

        self.datasets = []

        for i in range(len(environments)):
            images = original_images[i::len(environments)]
            labels = original_labels[i::len(environments)]
            self.datasets.append(dataset_transform(images, labels, environments[i]))

        self.input_shape = input_shape
        self.num_classes = num_classes

